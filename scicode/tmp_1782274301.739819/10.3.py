# Background: 
# The Ewald summation is a technique for calculating long-range Coulombic interactions in periodic systems.
# The alpha parameter controls the division of interactions into short-range (real space) and long-range 
# (reciprocal space) components. A Gaussian charge distribution with width parameter alpha is used to 
# partition the Coulomb potential into two complementary parts that converge faster than the original sum.
# 
# The alpha value is typically determined from the reciprocal lattice vectors to ensure an appropriate
# balance between real and reciprocal space summation. The standard approach is to use the magnitude of
# the smallest reciprocal lattice vector (or a related measure) as the basis for alpha. The formula is:
# 
# alpha = alpha_scaling / (2 * pi * r_min)
# 
# where r_min is related to the shortest distance in reciprocal space. A common choice is to compute
# the norm of the reciprocal lattice vectors and use their minimum or geometric mean as the scale.
# Alternatively, alpha can be derived from the volume of the reciprocal lattice cell or the 
# characteristic length scale. For robustness, alpha is often set as:
#
# alpha = alpha_scaling * sqrt(det(G)) / (2 * pi)
# where G is the reciprocal metric tensor (recvec @ recvec.T), and sqrt(det(G)) relates to the
# reciprocal space volume.

def get_alpha(recvec, alpha_scaling=5):
    '''Calculate the alpha value for the Ewald summation, scaled by a specified factor.
    Parameters:
        recvec (np.ndarray): A 3x3 array representing the reciprocal lattice vectors.
        alpha_scaling (float): A scaling factor applied to the alpha value. Default is 5.
    Returns:
        float: The calculated alpha value.
    '''
    
    # Compute the reciprocal metric tensor G = recvec @ recvec.T
    G = recvec @ recvec.T
    
    # Calculate the determinant of G
    det_G = np.linalg.det(G)
    
    # Ensure det_G is positive (it should be for a valid reciprocal lattice)
    det_G = np.abs(det_G)
    
    # Calculate alpha using the formula: alpha = alpha_scaling * sqrt(det(G)) / (2 * pi)
    alpha = alpha_scaling * np.sqrt(det_G) / (2 * np.pi)
    
    return alpha


# Background:
# In periodic systems, the lattice is represented by lattice vectors that define the unit cell.
# To evaluate long-range interactions (e.g., Coulombic forces) in an infinite periodic system,
# we need to consider contributions from multiple image cells. The Ewald summation technique
# requires evaluating interactions from atoms in neighboring periodic images of the unit cell.
#
# The lattice coordinates are generated by constructing integer linear combinations of the
# lattice vectors. For a given nlatvec parameter, we create a grid of lattice points by
# considering integer multiples of each lattice vector in the range [-nlatvec, nlatvec+1).
# This creates a cubic tiling of (2*nlatvec + 1)^3 lattice cells centered at the origin.
#
# The procedure is:
# 1. Generate all integer index triplets (i, j, k) where each index ranges from -nlatvec to nlatvec (inclusive).
# 2. For each triplet, compute the lattice coordinate as: latvec[0]*i + latvec[1]*j + latvec[2]*k
# 3. This is equivalent to computing the matrix product: indices @ latvec, where indices is an
#    array of shape ((2*nlatvec + 1)^3, 3) containing all index triplets.
# 4. The resulting lattice_coords array has shape ((2*nlatvec + 1)^3, 3) where each row is
#    a 3D Cartesian coordinate of a lattice point.

def get_lattice_coords(latvec, nlatvec=1):
    '''Generate lattice coordinates based on the provided lattice vectors.
    Parameters:
        latvec (np.ndarray): A 3x3 array representing the lattice vectors.
        nlatvec (int): The number of lattice coordinates to generate in each direction.
    Returns:
        np.ndarray: An array of shape ((2 * nlatvec + 1)^3, 3) containing the lattice coordinates.
    '''
    
    # Generate the range of indices for each direction: [-nlatvec, -nlatvec+1, ..., nlatvec]
    indices_1d = np.arange(-nlatvec, nlatvec + 1)
    
    # Create a meshgrid to generate all combinations of indices in 3D
    i_grid, j_grid, k_grid = np.meshgrid(indices_1d, indices_1d, indices_1d, indexing='ij')
    
    # Flatten the grids to create an array of shape ((2*nlatvec+1)^3, 3)
    indices = np.column_stack([i_grid.flatten(), j_grid.flatten(), k_grid.flatten()])
    
    # Compute lattice coordinates as linear combinations of lattice vectors
    # indices @ latvec gives shape ((2*nlatvec+1)^3, 3)
    lattice_coords = indices @ latvec
    
    return lattice_coords



# Background: In computational chemistry and materials science, calculating pairwise distances
# between particles is fundamental for evaluating interaction energies and forces. When computing
# the Ewald summation or other pair-based potentials, we need to:
# 1. Identify all unique pairs of particles (avoiding self-interactions and double counting)
# 2. Calculate the distance vectors between each pair
# 3. Maintain indexing information to trace results back to the original particles
#
# For nparticles particles, there are C(nparticles, 2) = nparticles * (nparticles - 1) / 2
# unique pairs. The distance vector from particle i to particle j is simply configs[j] - configs[i].
# We typically iterate through all pairs where i < j to avoid redundancy and self-interactions.
#
# This distance matrix is essential for:
# - Computing real-space Ewald summation contributions (short-range interactions)
# - Calculating Coulombic forces and energies
# - Evaluating pair potentials in molecular dynamics simulations
# The pair indices allow us to later distribute computed energies and forces back to the
# correct particles in the system.

def distance_matrix(configs):
    '''Args:
        configs (np.array): (nparticles, 3)
    Returns:
        distances (np.array): distance vector for each particle pair. Shape (npairs, 3), where npairs = (nparticles choose 2)
        pair_idxs (list of tuples): list of pair indices
    '''
    
    nparticles = configs.shape[0]
    
    # Generate all unique pairs (i, j) where i < j
    pair_idxs = []
    distances_list = []
    
    for i in range(nparticles):
        for j in range(i + 1, nparticles):
            pair_idxs.append((i, j))
            # Distance vector from particle i to particle j
            distances_list.append(configs[j] - configs[i])
    
    # Convert list to numpy array
    distances = np.array(distances_list)
    
    return distances, pair_idxs


