import numpy as np
from scipy.special import erfc

# Background: 
# The Ewald summation is a technique for calculating long-range Coulombic interactions in periodic systems.
# The alpha parameter controls the division of interactions into short-range (real space) and long-range 
# (reciprocal space) components. A Gaussian charge distribution with width parameter alpha is used to 
# partition the Coulomb potential into two complementary parts that converge faster than the original sum.
# 
# The alpha value is typically determined from the reciprocal lattice vectors to ensure an appropriate
# balance between real and reciprocal space summation. The standard approach is to use the magnitude of
# the smallest reciprocal lattice vector (or a related measure) as the basis for alpha. The formula is:
# 
# alpha = alpha_scaling / (2 * pi * r_min)
# 
# where r_min is related to the shortest distance in reciprocal space. A common choice is to compute
# the norm of the reciprocal lattice vectors and use their minimum or geometric mean as the scale.
# Alternatively, alpha can be derived from the volume of the reciprocal lattice cell or the 
# characteristic length scale. For robustness, alpha is often set as:
#
# alpha = alpha_scaling * sqrt(det(G)) / (2 * pi)
# where G is the reciprocal metric tensor (recvec @ recvec.T), and sqrt(det(G)) relates to the
# reciprocal space volume.

def get_alpha(recvec, alpha_scaling=5):
    '''Calculate the alpha value for the Ewald summation, scaled by a specified factor.
    Parameters:
        recvec (np.ndarray): A 3x3 array representing the reciprocal lattice vectors.
        alpha_scaling (float): A scaling factor applied to the alpha value. Default is 5.
    Returns:
        float: The calculated alpha value.
    '''
    
    # Compute the reciprocal metric tensor G = recvec @ recvec.T
    G = recvec @ recvec.T
    
    # Calculate the determinant of G
    det_G = np.linalg.det(G)
    
    # Ensure det_G is positive (it should be for a valid reciprocal lattice)
    det_G = np.abs(det_G)
    
    # Calculate alpha using the formula: alpha = alpha_scaling * sqrt(det(G)) / (2 * pi)
    alpha = alpha_scaling * np.sqrt(det_G) / (2 * np.pi)
    
    return alpha


# Background:
# In periodic systems, the lattice is represented by lattice vectors that define the unit cell.
# To evaluate long-range interactions (e.g., Coulombic forces) in an infinite periodic system,
# we need to consider contributions from multiple image cells. The Ewald summation technique
# requires evaluating interactions from atoms in neighboring periodic images of the unit cell.
#
# The lattice coordinates are generated by constructing integer linear combinations of the
# lattice vectors. For a given nlatvec parameter, we create a grid of lattice points by
# considering integer multiples of each lattice vector in the range [-nlatvec, nlatvec+1).
# This creates a cubic tiling of (2*nlatvec + 1)^3 lattice cells centered at the origin.
#
# The procedure is:
# 1. Generate all integer index triplets (i, j, k) where each index ranges from -nlatvec to nlatvec (inclusive).
# 2. For each triplet, compute the lattice coordinate as: latvec[0]*i + latvec[1]*j + latvec[2]*k
# 3. This is equivalent to computing the matrix product: indices @ latvec, where indices is an
#    array of shape ((2*nlatvec + 1)^3, 3) containing all index triplets.
# 4. The resulting lattice_coords array has shape ((2*nlatvec + 1)^3, 3) where each row is
#    a 3D Cartesian coordinate of a lattice point.

def get_lattice_coords(latvec, nlatvec=1):
    '''Generate lattice coordinates based on the provided lattice vectors.
    Parameters:
        latvec (np.ndarray): A 3x3 array representing the lattice vectors.
        nlatvec (int): The number of lattice coordinates to generate in each direction.
    Returns:
        np.ndarray: An array of shape ((2 * nlatvec + 1)^3, 3) containing the lattice coordinates.
    '''
    
    # Generate the range of indices for each direction: [-nlatvec, -nlatvec+1, ..., nlatvec]
    indices_1d = np.arange(-nlatvec, nlatvec + 1)
    
    # Create a meshgrid to generate all combinations of indices in 3D
    i_grid, j_grid, k_grid = np.meshgrid(indices_1d, indices_1d, indices_1d, indexing='ij')
    
    # Flatten the grids to create an array of shape ((2*nlatvec+1)^3, 3)
    indices = np.column_stack([i_grid.flatten(), j_grid.flatten(), k_grid.flatten()])
    
    # Compute lattice coordinates as linear combinations of lattice vectors
    # indices @ latvec gives shape ((2*nlatvec+1)^3, 3)
    lattice_coords = indices @ latvec
    
    return lattice_coords


# Background: In computational chemistry and materials science, calculating pairwise distances
# between particles is fundamental for evaluating interaction energies and forces. When computing
# the Ewald summation or other pair-based potentials, we need to:
# 1. Identify all unique pairs of particles (avoiding self-interactions and double counting)
# 2. Calculate the distance vectors between each pair
# 3. Maintain indexing information to trace results back to the original particles
#
# For nparticles particles, there are C(nparticles, 2) = nparticles * (nparticles - 1) / 2
# unique pairs. The distance vector from particle i to particle j is simply configs[j] - configs[i].
# We typically iterate through all pairs where i < j to avoid redundancy and self-interactions.
#
# This distance matrix is essential for:
# - Computing real-space Ewald summation contributions (short-range interactions)
# - Calculating Coulombic forces and energies
# - Evaluating pair potentials in molecular dynamics simulations
# The pair indices allow us to later distribute computed energies and forces back to the
# correct particles in the system.

def distance_matrix(configs):
    '''Args:
        configs (np.array): (nparticles, 3)
    Returns:
        distances (np.array): distance vector for each particle pair. Shape (npairs, 3), where npairs = (nparticles choose 2)
        pair_idxs (list of tuples): list of pair indices
    '''
    
    nparticles = configs.shape[0]
    
    # Generate all unique pairs (i, j) where i < j
    pair_idxs = []
    distances_list = []
    
    for i in range(nparticles):
        for j in range(i + 1, nparticles):
            pair_idxs.append((i, j))
            # Distance vector from particle i to particle j
            distances_list.append(configs[j] - configs[i])
    
    # Convert list to numpy array
    distances = np.array(distances_list)
    
    return distances, pair_idxs


# Background:
# The real-space part of the Ewald summation evaluates the short-range Coulombic interactions
# between particles and their periodic images. The complementary error function erfc() is used to
# weight these interactions, where the parameter alpha controls the decay rate.
#
# For each pair of particles (i, j), we need to sum over all lattice images n to account for
# periodic boundary conditions. The lattice image contribution accounts for interactions with
# particles in neighboring unit cells.
#
# The mathematical form is:
# c_ij = sum over all lattice cells n of [erfc(alpha * |r_ij + n*L|) / |r_ij + n*L|]
#
# where:
# - r_ij is the distance vector between particles i and j
# - n*L is the lattice displacement vector for cell n
# - alpha is the Ewald parameter controlling real/reciprocal space balance
# - erfc is the complementary error function
#
# Input shapes:
# - distances: (natoms, npairs, 1, 3) - distance vectors for each pair
# - lattice_coords: (natoms, 1, ncells, 3) - lattice cell displacements
#
# The shapes are designed for broadcasting: adding lattice_coords to distances gives
# (natoms, npairs, ncells, 3) representing all pair-lattice combinations.
#
# Algorithm:
# 1. Compute displaced distance vectors by adding lattice coordinates to the base distances
#    (broadcasting over the lattice dimension): shape becomes (natoms, npairs, ncells, 3)
# 2. Calculate the magnitudes (norms) of these displaced vectors: (natoms, npairs, ncells)
# 3. Avoid division by zero by replacing zero magnitudes with a small value or NaN-safe operation
# 4. Compute erfc(alpha * magnitudes) for each displaced vector
# 5. Divide by magnitudes to get the weighted contribution
# 6. Sum over all lattice cells (axis 2) to get the final c_ij for each pair: (natoms, npairs)
# 7. Return the result reshaped/squeezed to (npairs,)

def real_cij(distances, lattice_coords, alpha):
    '''Calculate the real-space terms for the Ewald summation over particle pairs.
    Parameters:
        distances (np.ndarray): An array of shape (natoms, npairs, 1, 3) representing the distance vectors between pairs of particles where npairs = (nparticles choose 2).
        lattice_coords (np.ndarray): An array of shape (natoms, 1, ncells, 3) representing the lattice coordinates.
        alpha (float): The alpha value used for the Ewald summation.
    Returns:
        np.ndarray: An array of shape (npairs,) representing the real-space sum for each particle pair.
    '''
    
    # Add lattice coordinates to distance vectors via broadcasting
    # distances: (natoms, npairs, 1, 3)
    # lattice_coords: (natoms, 1, ncells, 3)
    # result: (natoms, npairs, ncells, 3)
    displaced_distances = distances + lattice_coords
    
    # Calculate magnitudes of displaced distance vectors
    # shape: (natoms, npairs, ncells)
    magnitudes = np.linalg.norm(displaced_distances, axis=-1)
    
    # Avoid division by zero: replace zero magnitudes with a large value
    # (these will contribute zero to the sum due to erfc behavior)
    magnitudes_safe = np.where(magnitudes == 0, np.inf, magnitudes)
    
    # Compute erfc(alpha * magnitudes) / magnitudes for each displaced vector
    # shape: (natoms, npairs, ncells)
    weighted_interactions = erfc(alpha * magnitudes_safe) / magnitudes_safe
    
    # Sum over all lattice cells (axis 2)
    # shape: (natoms, npairs)
    cij = np.sum(weighted_interactions, axis=2)
    
    # Squeeze/reshape to (npairs,) - assuming natoms dimension is 1 or needs flattening
    # The result is indexed by pair index
    cij_flat = cij.reshape(-1)
    
    return cij_flat



# Background:
# The real-space cross term in the Ewald summation calculates the Coulombic interaction energy
# between all pairs of particles in the system, accounting for periodic images via the lattice.
# This is a core component of the Ewald summation method for calculating long-range electrostatic
# interactions in periodic systems.
#
# The formula E_real-cross = sum_i sum_{j>i} q_i * q_j * c_ij represents the sum of pairwise
# electrostatic interactions where:
# - q_i and q_j are the charges of particles i and j
# - c_ij is the lattice-summed Coulombic interaction term computed via the real_cij function,
#   which includes all periodic image contributions with the erfc weighting function
#
# The key insight is that c_ij already encodes the full periodic boundary condition treatment
# for the real-space component. We simply need to:
# 1. Compute the distance vectors between all pairs of particles (using distance_matrix)
# 2. Reshape these distances for broadcasting with lattice coordinates
# 3. Apply the real_cij function to get the lattice-summed interaction coefficients
# 4. Multiply each coefficient by the product of the two particle charges
# 5. Sum over all pairs to get the total real-space cross energy
#
# The function handles three types of interactions:
# - Atom-atom (ion-ion) interactions
# - Atom-electron (ion-electron) interactions  
# - Electron-electron interactions
# These are naturally captured by considering all pairs from the combined particle set.

def sum_real_cross(atom_charges, atom_coords, configs, lattice_coords, alpha):
    '''Calculate the sum of real-space cross terms for the Ewald summation.
    Parameters:
        atom_charges (np.ndarray): An array of shape (natoms,) representing the charges of the atoms.
        atom_coords (np.ndarray): An array of shape (natoms, 3) representing the coordinates of the atoms.
        configs (np.ndarray): An array of shape (nelectrons, 3) representing the configurations of the electrons.
        lattice_coords (np.ndarray): An array of shape (ncells, 3) representing the lattice coordinates.
        alpha (float): The alpha value used for the Ewald summation.
    Returns:
        float: The sum of ion-ion, electron-ion, and electron-electron cross terms.
    '''
    
    natoms = atom_coords.shape[0]
    nelectrons = configs.shape[0]
    ncells = lattice_coords.shape[0]
    
    # Combine atom and electron coordinates
    all_coords = np.vstack([atom_coords, configs])
    ntotal = all_coords.shape[0]
    
    # Combine charges: atoms first, then electrons (charge = -1 each)
    all_charges = np.concatenate([atom_charges, np.full(nelectrons, -1.0)])
    
    # Compute distance vectors and pair indices for all particles
    distances, pair_idxs = distance_matrix(all_coords)
    
    # Reshape distances for broadcasting with lattice coordinates
    # distances shape: (npairs, 3) -> (ntotal, npairs, 1, 3)
    # This representation allows broadcasting with lattice_coords of shape (ncells, 3)
    distances_reshaped = distances.reshape(1, -1, 1, 3)
    
    # Reshape lattice coordinates for broadcasting
    # lattice_coords shape: (ncells, 3) -> (1, 1, ncells, 3)
    lattice_coords_reshaped = lattice_coords.reshape(1, 1, -1, 3)
    
    # Compute the real-space interaction coefficients using real_cij
    # This function returns shape (npairs,)
    cij = real_cij(distances_reshaped, lattice_coords_reshaped, alpha)
    
    # Calculate the energy as the sum of q_i * q_j * c_ij over all pairs
    energy = 0.0
    for pair_idx, (i, j) in enumerate(pair_idxs):
        qi = all_charges[i]
        qj = all_charges[j]
        energy += qi * qj * cij[pair_idx]
    
    return energy


